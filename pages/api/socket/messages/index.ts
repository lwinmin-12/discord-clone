import { currentProfilePages } from "@/lib/currentProfilePages";
import { db } from "@/lib/db";
import { errorResponse, unauthorizeResponse } from "@/lib/globalFunction";
import { NextApiResponseServerIo } from "@/types";
import { NextApiRequest } from "next";

export default async function handler(
  req: NextApiRequest,
  res: NextApiResponseServerIo
) {
  if (req.method !== "POST") {
    return res.status(405).json({ error: "Method not allowed" });
  }

  try {
    const profile = await currentProfilePages(req);
    const { content, fileUrl } = req.body;
    const { serverId, channelId } = req.query;

    if (!profile) {
      return unauthorizeResponse();
    }

    if (!serverId) {
      return errorResponse("Server ID missing");
    }

    if (!channelId) {
      return errorResponse("Channel ID missing");
    }

    if (!content) {
      return errorResponse("Content missing");
    }

    const server = await db.server.findFirst({
      where: {
        id: serverId as string,
        members: {
          some: {
            profileId: profile.id,
          },
        },
      },
      include: {
        members: true,
      },
    });

    if (!server) {
      return errorResponse("Server not found");
    }

    const channel = await db.channel.findFirst({
      where: {
        id: channelId as string,
        serverId: serverId as string,
      },
    });

    if (!channel) {
      return errorResponse("Channel not found");
    }

    const member = server.members.find(
      (ea: any) => ea.profileId === profile.id
    );

    if (!member) {
      return errorResponse("Member not found");
    }

    const message = await db.message.create({
      data: {
        content,
        fileUrl,
        channelId: channelId as string,
        memberId: member.id,
      },
      include: {
        member: {
          include: {
            profile: true,
          },
        },
      },
    });

    const channelKey = `chat:${channelId}:messages`;

    res?.socket?.server?.io?.emit(channelKey, message);

    return res.status(200).json(message);
  } catch (error) {
    console.log("[MESSAGES_POST]", error);
    errorResponse("Internal server error", 500);
  }
}
